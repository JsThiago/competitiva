function shortestPath($grid, $k,$countOnes,$count,$x,$y) {
    if($grid[$y][$x] === 1) $countOnes++;
    if($countOnes === $k) return -1;
    if(($y === count($grid)-1) && ($x === count($grid[0])-1)) return $count;
    $count++;
    $nextX = $x;
    $nextY = $y;
    $right = -1;
    $down = -1;
    if($y < count($grid)-1){
        $nextY = $y + 1;
        $down = shortestPath($grid,$k,$countOnes,$count,$x,$nextY);
    }
    if($x < count($grid[0])-1){
        $nextX = $x + 1;
        $right = shortestPath($grid,$k,$countOnes,$count,$nextX,$y);
    }   
    if($right < $down && ($right !==-1)){
        return $right;
    }
    if($down !==-1){
        return $down;
    }
    return -1;
    
   
}


